<?php
    # CONDITIONALS

    /*
        == : value comparison operator
        === :value as well datatype comparison operator
        > : greater operator
        < : less operator
        <= : less then equal to operator
        >= : greater then or equal to operator
        != : Not equal to operator
        !== : Not equal to operator (value as well dataType)
        OR, AND etc
    */

    $num = 5;

    if($num == 5){
        echo 'Number is 5 <br>';
    }else{
        echo 'Something else!!!<br>';
    }

    
    if($num > 5){
        echo 'Number is greater then 5 <br>';
    }else{
        echo 'Not greater then 5 <br>';
    }

    
    if($num != 5){
        echo 'Number is Not 5 <br>';
    }else{
        echo 'It is Five!!!<br>';
    }


?>
